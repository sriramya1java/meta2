<template>
  <div class="breadcrumb">
    <ul>
      <li v-for="(breadcrumb, idx) in breadcrumbList"
      :key="idx"
      @click="routeTo(idx)"
      :class="{'linked': !!breadcrumbs.link}">
        {{ breadcrumb.name}}
      </li>
    </ul>
  </div>
</template>
<script>
  export default {
    name: 'Breadcrumbs',
    data () {
      return {
        breadcrumbList: [],
        breadcrumbs: []
      }
    },
    mounted () {
      this.updateList()
    },
    watch: {
      '$route' () {
        this.updateList()
      }
    },
    methods: {
      routeTo (pRouteTo) {
        if (this.breadcrumbList[pRouteTo].link) this.$router.push(this.breadcrumbList[pRouteTo].link)
      },
      updateList () {
        this.breadcrumbList = this.$route.meta.breadcrumb
      }
    }
  }
</script>
